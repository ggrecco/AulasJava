package fundamentos;

public class TiposPrimitivos {
	public static void main(String[] args) {		
		double a = 1-23; // interpreta como operação
		double b = 2_30; //interpreta como virgula
		float c = 11_111F;
		float d = 1_111F;
		
		double res = a + b;
		System.out.println(res);

		float result = c + d;
		System.out.println(result);
		
		
		System.out.println();
	}
}
